    public void tipoBusqueda(){
        if(rbtnNombre.isSelected()==true){
            lblBusqueda.setText("Introduce el nombre:");
            lblBusqueda.enable();
            txtBusqueda.enable();
        }
        if(rbtnApellido.isSelected()==true){
            lblBusqueda.setText("Introduce el apellido:");
            lblBusqueda.enable();
            txtBusqueda.enable();
        }
        if(rbtnClave.isSelected()==true){
            lblBusqueda.setText("Introduce la clave del usuario:");
            lblBusqueda.enable();
            txtBusqueda.enable();
        }
        if(rbtnMatricula.isSelected()==true){
            lblBusqueda.setText("Introduce la matricula del usuario:");
            lblBusqueda.enable();
            txtBusqueda.enable();
        }
        if(rbtnTodos.isSelected()==true){
            lblBusqueda.setText("Encontrar todos los usuarios");
            lblBusqueda.disable();
            txtBusqueda.disable();
        }
    }
